"""FSM состояния для команды /imagine.

Управляет генерацией изображений:
1. Выбор модели (DALL-E, FLUX, и т.д.)
2. Ввод промпта (текстовое описание изображения)
3. Генерация и отправка результата
"""

from aiogram.fsm.state import State, StatesGroup


class ImagineStates(StatesGroup):
    """Состояния FSM для команды /imagine.

    Поток работы:
    1. /imagine → показываем выбор модели → waiting_for_model_selection
    2. Пользователь выбирает модель → сохраняем выбор → waiting_for_prompt
    3. Пользователь вводит промпт → генерируем изображение → отправляем → выход из FSM
    4. Если пользователь хочет ещё — он снова использует /imagine

    Attributes:
        waiting_for_model_selection: Ожидаем выбор модели изображений (inline-кнопка).
        waiting_for_prompt: Ожидаем текстовое описание изображения от пользователя.
    """

    # Шаг 1: Выбор модели
    # Пользователь видит inline-клавиатуру с доступными моделями изображений
    waiting_for_model_selection = State()

    # Шаг 2: Ввод промпта
    # Пользователь вводит текстовое описание желаемого изображения
    waiting_for_prompt = State()
